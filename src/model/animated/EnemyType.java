package model.animated;

/**
 * Enumeration of enemy types.
 */
public enum EnemyType {

    /**
     * Create Static Simple Direction Shot Enemy.
     */
    SIMPLE,

    /**
     * Create Static Aimed Bullet Enemy.
     */
    SIMPLEAIMED,

    /**
     * Create Simple Direction Moved Enemy.
     */
    SIMPLEMOVE,

    /**
     * Create Followed Direction Moved Enemy.
     */
    FOLLOWED,

    /**
     * Create Static Enemy Four Way Diagonal.
     */
    DIAGONAL,

    /**
     * Create Static Enemy Four Way Straight.
     */
    STRAIGHT;
}
